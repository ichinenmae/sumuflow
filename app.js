function handleAction(target) {
  const action = target.dataset.action;
  const type = target.dataset.type;
  const id = target.dataset.id;
  if (!action) return;

  if (action === "new-payment") openEntryDialog("payment");
  if (action === "new-income") openEntryDialog("income");
  if (action === "edit") openEntryDialog(type, findItem(type, id));
  if (action === "duplicate") duplicateItem(type, id);
  if (action === "delete") deleteItem(type, id);
  if (action === "toggle-paid") togglePaid(id);
  if (action === "export-json") exportJson();
  if (action === "import-json") importFile.click();
  if (action === "reset-data") resetData();
  if (action === "clear-ledger") clearLedger();
}

function findItem(type, id) {
  return (type === "payment" ? state.payments : state.incomes).find((item) => item.id === id);
}

function duplicateItem(type, id) {
  const source = findItem(type, id);
  if (!source) return;
  const copy = { ...source, id: makeId(), name: `${source.name} コピー`, status: source.status || "planned" };
  if (type === "payment") state.payments.push(copy);
  else state.incomes.push(copy);
  saveAndRender();
}

function deleteItem(type, id) {
  if (!confirm("削除しますか？")) return;
  if (type === "payment") state.payments = state.payments.filter((item) => item.id !== id);
  else state.incomes = state.incomes.filter((item) => item.id !== id);
  saveAndRender();
}

function togglePaid(id) {
  const payment = state.payments.find((item) => item.id === id);
  if (!payment) return;
  payment.status = payment.status === "paid" ? "planned" : "paid";
  saveAndRender();
}

function adjustBalance(actualBalance) {
  const calculatedBalance = currentBalance();
  const difference = Number(actualBalance) - calculatedBalance;
  if (difference === 0) {
    state.balance.updatedAt = new Date().toISOString();
    saveAndRender();
    showToast("差額はありません");
    return;
  }

  const adjustment = {
    id: makeId(),
    name: "残高調整",
    amount: Math.abs(difference),
    date: todayInput()
  };

  if (difference > 0) {
    state.incomes.push(adjustment);
    showToast("差額を収入として調整しました");
  } else {
    state.payments.push({ ...adjustment, status: "paid" });
    showToast("差額を支出として調整しました");
  }

  state.balance.updatedAt = new Date().toISOString();
  saveAndRender();
}

function exportJson() {
  state.backup.lastBackupAt = new Date().toISOString();
  StorageService.save(state);
  const blob = new Blob([JSON.stringify(state, null, 2)], { type: "application/json" });
  const url = URL.createObjectURL(blob);
  const anchor = document.createElement("a");
  anchor.href = url;
  anchor.download = `delivery-cashflow-${todayInput()}.json`;
  anchor.click();
  URL.revokeObjectURL(url);
  render();
  showToast("データを保存しました");
}

function resetData() {
  if (!confirm("すべてのデータを初期化しますか？")) return;
  StorageService.clear();
  state = createSeedData();
  saveAndRender();
}

function clearLedger() {
  if (!confirm("支払いと入金予定をすべて削除し、現在残高も0にします。設定は残ります。実行しますか？")) return;
  state.payments = [];
  state.incomes = [];
  state.balance.amount = 0;
  state.balance.updatedAt = new Date().toISOString();
  saveAndRender();
  showToast("入出金データと残高を削除しました");
}

function appendImportedData(imported) {
  state.payments = [
    ...state.payments,
    ...imported.payments.map((payment) => ({ ...payment, id: makeId() }))
  ];
  state.incomes = [
    ...state.incomes,
    ...imported.incomes.map((income) => ({ ...income, id: makeId() }))
  ];
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

