function dangerousPayments(simulation) {
  const byPayment = simulation
    .filter((row) => row.type === "payment" && row.judgement !== "OK")
    .map((row) => ({
      ...row,
      shortage: Math.max(0, -row.balance),
      requiredHours: state.settings.hourlyRate > 0 ? Math.max(0, -row.balance) / state.settings.hourlyRate : 0,
      requiredDeliveries: deliveriesForAmount(Math.max(0, -row.balance))
    }));

  const overdueRows = state.payments
    .filter((payment) => payment.status !== "paid" && paymentStatus(payment) === "期限超過")
    .map((payment) => ({
      date: payment.date,
      name: payment.name,
      amount: payment.amount,
      balance: currentBalance() - payment.amount,
      judgement: "期限超過",
      shortage: Math.max(0, payment.amount - currentBalance()),
      requiredHours: state.settings.hourlyRate > 0 ? Math.max(0, payment.amount - currentBalance()) / state.settings.hourlyRate : 0,
      requiredDeliveries: deliveriesForAmount(Math.max(0, payment.amount - currentBalance())),
      id: payment.id
    }));

  return [...overdueRows, ...byPayment]
    .sort((a, b) => rankJudgement(a.judgement) - rankJudgement(b.judgement) || a.date.localeCompare(b.date))
    .slice(0, 8);
}

function rankJudgement(value) {
  return { "期限超過": 0, NG: 1, "注意": 2, OK: 3 }[value] ?? 4;
}

